<p class="body_content_style1">Mercury is an essential element in the operation of fluorescent lighting; it allows the bulbs to be an efficient light source. Because CFLs contain trace amounts of mercury, it is important to educate yourself on proper use, recycling and disposal of these products.</p>

	<h4>The Facts about CFLs and Mercury</h4> 
	<ul>
		<li>Because CFLs use less electricity than traditional light bulbs, they reduce demand for electricity; that reduction means less mercury is emitted from power plants.</li> 
		<li>CFLs contain a very small amount of mercury &#8212; an average of 4 milligrams in each bulb.</li>
		<li>No mercury is released when the bulbs are intact or in use.</li>
	</ul>

	<h4>Why the EPA, DOE and others are promoting the use of CFLs</h4>
	<p>CFLs use significantly less energy than traditional light bulbs (75% less). If every home in America replaced just one incandescent light bulb with an ENERGY STAR qualified CFL, we would save enough energy to light more than 3 million homes and prevent greenhouse gas emissions equivalent to that of 800,000 cars. And even though CFLs contain a small amount of mercury that could ultimately end up in the environment, that amount is significantly less than the amount of mercury avoided as a result of the energy savings.</p>

	
	<h4>Recycling and Disposing of CFLs</h4>
	
	<p>Like any other product containing potentially hazardous materials that you use in your home, CFLs come with some special instructions.</p>
	
	<p><a class="body_content_style1" href="http://www.energystar.gov/ia/products/lighting/cfls/downloads/CFL_Cleanup_and_Disposal.pdf" target="_blank">Learn what you should do if a CFL breaks in your home</a> <img src="images/pdf_tiny.gif" alt="PDF" width="15" height="16" /> (27KB), and get EPA's recommendations for recycling and responsibly disposing of CFLs at the end of their life cycle.</p> 

