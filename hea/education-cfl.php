<h2>Earning the Government&#8217;s ENERGY STAR</h2>

<h4>ENERGY STAR qualified CFLs</h4>
	<ul>
		<li>ENERGY STAR qualified bulbs use about <strong>75 percent less energy</strong> than standard incandescent bulbs and last up to <strong>10 times longer.</strong></li>

		<li><strong>Save about $30 or more</strong> in electricity costs over each bulb&#8217;s lifetime.</li>

		<li>Produce about <strong>75 percent less heat,</strong> so they&#8217;re safer to operate and can cut energy costs associated with home cooling.</li>

		<li>Are available in different <a class="body_content_style1" href="education-cfl-sizes.php">sizes and shapes</a> to fit in almost any fixture, for indoors and outdoors.</li>

	</ul>
	
		<p><a class="body_content_style1" href="education-cfl-mercury.php">Learn</a> the facts about mercury in CFLs.</p>
		
		<p>Recycling information for ENERGY STAR qualified CFLs may be found at <a class="body_content_style1" href="http://www.epa.gov/bulbrecycling">www.epa.gov/bulbrecycling</a> or <a  class="body_content_style1" href="http://www.earth911.org">www.earth911.org</a>.</p>	 


<h4>How to Choose and Where to Use CFLs</h4>

<p>ENERGY STAR qualified CFLs provide the greatest savings in fixtures that are on for a substantial amount of time each day.  At a minimum, ENERGY STAR recommends installing qualified CFLs in fixtures that are used at least 15 minutes at a time or several hours per day.  The best fixtures to use qualified CFLs in are usually found in the following areas of your home:</p>
	<ul>
		<li>family and living rooms</li>
		<li>kitchen</li>
		<li>dining room</li>
		<li>bedrooms</li>
		<li>outdoors</li>

	</ul>

<h4>How to Choose the Right Light</h4>
<p>Matching the right CFL to the right kind of fixture helps ensure that it will perform properly and last a long time.</p>

<p>For example:</p> 
	<ul>
		<li>CFLs perform best in open fixtures that allow airflow, such as table and floor lamps, wall sconces, pendants, and outdoor fixtures.</li>

		<li>For recessed fixtures, it is better to use a reflector CFL than a spiral CFL since the design of the reflector evenly distributes the light down to your task area.</li>

		<li>If a light fixture is connected to a <strong>dimmer or three-way socket fixture,</strong> you&#8217;ll need to use a special ENERGY STAR qualified CFL designed to work in these applications. Make sure to look for CFLs that specify use with dimmers or three-way fixtures.</li>

		<li><a class="body_content_style1" href="education-cfl-qualified.php">Choose a qualified CFL</a> that offers a shade of white light that works best for you</a>. For example, while most CFLs provide warm or soft white light for your home, you could choose a cooler color for task lighting.</li>

		<li>To choose the ENERGY STAR qualified CFL with the right amount of light, find a qualified CFL that is labeled as equivalent to the incandescent bulb you are replacing.  Light bulb manufacturers include this information right on the product packaging to make it easy for consumers to choose the equivalent bulb. Common terms include <strong>&#8220;Soft White 60&#8221;</strong> or <strong>&#8220;60 Watt Replacement.&#8221;</strong></li>

</ul>

