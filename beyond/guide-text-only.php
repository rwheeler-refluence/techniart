<p class="body_content_style1">
<h2>Usage Tips</h2>

<p class="body_content_style1">To have the best experience possible, keep the following tips in mind:</p>

<ul>
	<li>Hold the base and not the glass to screw in the bulb.</li>
	<li>Read the packaging to see where each bulb should be used. Not all ENERGY STAR qualified CFLs are designed to work in every socket.</li>
	<li>Use ENERGY STAR qualified light bulbs in places where you will have the light on for at least 15 minutes at a time. Frequently turning a CFL on and off will shorten the bulb's lifetime.</li>
	<li>Most photocells and timers are not designed to work with CFLs. Check with your photocell or timer manufacturer for compatibility.</li>
	<li>When your CFL burns out, recycle it. Go to <a class="body_content_style1" href="http://www.epa.gov/bulbrecycling/" target="_blank">www.epa.gov/bulbrecycling</a> for recycling locations.</li>

	<li>If a bulb breaks follow the guidance found at <a class="body_content_style1" target="_blank" href="http://www.energystar.gov/ia/products/lighting/cfls/../../../partners/promotions/change_light/downloads/Fact_Sheet_Mercury.pdf">www.energystar.gov/CFLsandMercury</a> <img src="images/pdf_tiny.gif" alt="PDF" width="15" height="16" /> (60KB).</li>
</ul>

<p class="body_content_style1">Every time you are using an ENERGY STAR qualified product you are saving energy, money, and greenhouse gas emissions.</p>

<p class="body_content_style1">ENERGY STAR is a joint program of the U.S. Department of Energy and the U.S. Environmental Protection Agency helping us all save money and protect the environment through energy efficient products and practices.</p>

<br>
<h2>Light Temperature Tips: Choose Your Mood</h2>
<p class="body_content_style1">While most CFLs come in "warm" colors to match the yellowish light of incandescent bulbs, you can also choose "cooler" colors with whiter and bluish hues for reading and task lighting. Color in lighting is measured on the Kelvin scale (K) and is marked on CFL packaging. For warmer color look for 2700&#8211;3000K, 3500&#8211;4100K gives a bright white light and 5000&#8211;6500K is bluer and most like daylight.</p>

<br>
<h2>Bulb-Specific Tips: How to Choose</h2>
<h3>Spiral Bulbs</h3>
<p class="body_content_style1">If these spiral-shaped light bulbs look familiar it's because they're the most popular type of Compact Fluorescent Lamp (CFL). Spiral CFLs create the same amount of light as traditional incandescent bulbs, but use less energy.</p>

<p class="body_content_style1">Many traditional bulbs around your home (from 60w to 150w) can be replaced with spirals. There are spirals for dimmers and three-way switches (just check the packaging). Spirals also come in a variety of colors like soft white, natural light, or daylight.</p>

<h3>Covered A-Shaped</h3>
A-shaped bulbs combine the efficiency of the spiral bulbs, with the look and feel of the traditional incandescents. These products are great for consumers who don't like the look of the spiral bulbs but still want efficient lighting. You can use A-Line bulbs wherever you used to use traditional incandescents. Check the packaging for compatibility with dimmers and three-way fixtures.

<h3>Covered Globe</h3>
<p class="body_content_style1">Globe-shaped bulbs are ideal for use where you can see the bulbs, like bathroom vanity bars and ceiling pendants. A globe bulb is basically a spiral bulb with a decorative cover.</p>
<p class="body_content_style1">Like other covered CFLs, globe bulbs need a little time to "warm up" and reach full brightness. But be patient &#8212; ENERGY STAR qualified light bulbs generate just as much light as traditional bulbs, while using less energy.</p>

<h3>Tubed Bulbs</h3>
<p class="body_content_style1">Some of the first ENERGY STAR qualified light bulbs were tube shaped. Basically straight versions of the spiral bulbs, tubed bulbs work well in lamps that have slender covers such as wall sconces.</p>

<h3>Candle Bulbs</h3>
<p class="body_content_style1">These products are ideal for use in decorative fixtures where you can see the light bulb. The sleek shape also allows you to use them in tight fitting light fixtures where a covered globe won't fit.</p>

<h3>Indoor Reflector Bulbs</h3>
<p class="body_content_style1">Reflector bulbs are perfect for providing directional light &#8212; think of recessed ceiling lights in kitchens or ceiling fans. Indoor reflector bulbs are much smaller then those that are designed for outdoor use. Some indoor reflector bulbs can be used with a dimmer &#8212; the packaging will tell you.</p>

<h3>Outdoor Reflector Bulbs</h3>

<p class="body_content_style1">For use outside, reflector bulbs are sealed to withstand the rain and snow. Because of this, they're usually much larger then the reflectors designed for use inside. Don't use the outdoor reflectors with timers, photocells, and motion sensors because you could shorten the life of the bulbs.</p>

<h3>3-Way CFLs</h3>
<p class="body_content_style1">Fixtures or lamps with three-way switches require the use of a three-way CFL. Check the packaging to make sure that the bulb is intended for this use. Installing three-way CFLs may require extra effort since they can be slightly larger than their matching incandescents, but they still use one-third as much electricity. Three-way bulbs typically come in Soft White color temperature.</p>

<h3>Dimmable CFLs</h3>
<p class="body_content_style1">Fixtures or lamps with dimmer switches require the use of dimmable CFLs. Not all CFLs are dimmable so check the packaging to make sure it is. Dimmable CFLs work differently than incandescent bulbs. Incandescents dim smoothly from 100% of their light output to no output and their light color changes from a bright white to a warmer yellow. Dimmable CFLs maintain light color more consistently and dim to 10%&#8211;40% of its original brightness. Dimmable bulbs typically come in Soft White color temperature.</p>

<br>
<h2>Light Fixture-Specific Tips: Where to Use</h2>

<h3>Ceiling Fixtures</h3>
<p class="body_content_style1">For ceiling fixtures, spiral or tubed ENERGY STAR qualified bulbs are an economical choice. Ensure your fixture allows airflow to prevent excessive heat from shortening the life or decreasing the amount of light the CFL gives off.</p>

<p class="body_content_style1">If your ceiling fixture is hooked up to a dimmer switch, make sure you only use dimmable bulbs or else the light bulbs won't dim and might even burn out sooner.</p>

<h3>Pendant Fixtures</h3>
<p class="body_content_style1">While bare bulbs can be used, most people prefer the look of covered ENERGY STAR qualified light bulbs in their pendant fixtures. Covered bulbs come in both traditional "A" or globe shapes.</p>

<p class="body_content_style1">If your pendant fixture is hooked up to a dimmer switch, make sure you only use dimmable bulbs or else the light bulbs won't dim and might even burn out sooner.</p>

<h3>Ceiling Fans</h3>

<p class="body_content_style1">For ceiling fans, you have a variety of options. Spiral bulbs can be used but most people prefer the look of covered light bulbs such as "A"-shape, candles, or small reflectors. For some ceiling fans, the size of the CFL will be important. A lot of manufacturers are developing other CFLs for use specifically in ceiling fans.</p>

<p class="body_content_style1">If your ceiling fan is hooked up to a dimmer switch, make sure you only use dimmable bulbs or else the light bulbs won't dim and might even burn out sooner.</p>

<h3>Wall Sconces</h3>
<p class="body_content_style1">Due to their smaller sizes, spiral or bullet ENERGY STAR qualified light bulbs will work well in wall sconces.</p>

<p class="body_content_style1">If your sconce is hooked up to a dimmer switch, make sure you only use dimmable bulbs or else the light bulbs won't dim and might even burn out sooner.</p>

<h3>Recessed Cans</h3>
<p class="body_content_style1">Indoor reflector light bulbs work best in recessed cans because they are specially designed to direct the light out of the fixture and to withstand the heat buildup that occurs in these fixtures.</p>

<p class="body_content_style1">If your recessed cans use a dimmer switch, make sure you buy reflectors that are able to dim. The packaging will tell you whether or not you can use them with a dimmer.</p>

<h3>Outdoor Covered Fixtures</h3>
<p class="body_content_style1">Spiral or tubed ENERGY STAR qualified light bulbs are both appropriate to use in outdoor covered fixtures where the weather can't harm them.</p>

<p class="body_content_style1">If you live in a cold climate check the packaging for starting temperatures to make sure the bulb will work properly.</p>

<p class="body_content_style1">Check with the manufacturer of electronic controls like photocells, timers and motion sensors for compatibility with CFLs.</p>

<h3>Outdoor Exposed Fixtures</h3>

<p class="body_content_style1">ENERGY STAR qualified Outdoor flood light bulbs are recommended for outdoor exposed fixtures. These bulbs have special cases that protect them from nature's elements.</p>

<p class="body_content_style1">Placing a bare spiral CFL in an open outdoor fixture exposes the tubing and electronics to the elements and is likely to result in an early failure.</p>

<p class="body_content_style1">If you live in a cold climate check the packaging for starting temperatures to make sure the bulb will work properly.</p>

