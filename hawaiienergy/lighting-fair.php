<span class="section_heading_style1"><b>What is a Lighting Fair?</b></span><br>
<span class="body_content_style1"><i>Energy Saving Lighting Fairs</i> are promotional sales events that educate residential customers about ENERGY STAR<sup>&reg;</sup> CFLs and fixtures. Customers learn about the feautres and benefits of energy efficient lighting technologies and can purchase products at deeply discounted prices.  Discounted prices are made possible by electric utility companies, energy service providers or transformation groups that sponsor each event.
<br><br>
<i>Lighting Fairs</i> are created by TechniArt and hosted by commercial businesses, state agencies, public events, home shows, state and town fairs, and fundraisers.  They are aimed at residential consumers and deliver a wide selection of quality ENERGY STAR CFLs and fixtures.  We offer hands on demonstrations that provide education and information about ENERGY STAR lighting and other utility conservation programs and services.
<br><br>
<i>Energy Saving Lighting Fairs</i> offer an atmosphere of enthusiasm and education about ENERGY STAR lighting.  The one-on-one interaction with customers allows TechniArt's staff to teach individuals about the featurs and benefits of CFLs and help dispel misconceptions about their performance.  The wide variety of specialty CFLs offered for sale showcase CFL alternatives and llows current users of spiral CFLs the opportunity to further retrofit their homes.  In addition, the personalized communication between TechniArt staff and the consumer is a perfect opportunity to promote additional conservation programs.<br><br>
