Lighting &amp; Appliances</h1><h2 class="sublevel-page-subheader">Get $30 for turning in your old, working dehumidifier!</h2><div><p>Do you have an old, working dehumidifier that you'd like to upgrade or clean out? Recycle it with Mass Save and you can receive a $30 rebate! Most towns charge residents to recycle their appliances. With this limited-time offer, if you live in a participating town, or can make it to one of the open events, not only can you recycle your dehumidifier for free, but Mass Save will pay you $30 to do so! All you have to do is the following:</p>
<ol>
    <li>Find out if your town is participating or if there's an open event in your area. Additional events are still being scheduled for 2017, so continue to check back if there is not an event currently scheduled near you.</li>
    <li>Bring an old, working dehumidifier to your town's event or to an open event.</li>
    <li>Drop off your dehumidifier and receive a rebate form from event staff.</li>
    <li>Complete the rebate form and submit it through one of the following methods:
    <ol type="a"><li>Complete the rebate form at the event and leave it with the event staff,</li><li>Fill out the rebate form at home and send it in through mail to the address listed on the rebate form, or</li><li>Fill out the rebate form at home and submit it online at <a href="http://www.MassSave.com/Savings" target="_blank">MassSave.com/Savings</a>.</li></ol></li>
    <li>Receive your $30 recycling rebate by mail 4 - 6 weeks after the form is submitted.</li>
</ol>
<p>The following restrictions apply to this limited-time offer:</p>
<ul>
    <li>Limit two dehumidifier turn-in rebates per Mass Save Sponsor residential electric account per calendar year (commercial accounts are not eligible).</li>
    <li>Eligible dehumidifiers must be sized appropriately for residential applications (20 to 70 pints).</li>
    <li>Eligible dehumidifiers must be in working condition</li>
    <li>If participating through a town-specific event, you must be a resident of that town and provide ID for proof of residency.</li>
</ul>
<p>It's that easy, you'll get $30, clean out some storage space, and help the environment!</p>
