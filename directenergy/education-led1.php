    <div id="pg_hd">
    <h1>What is Solid-State Lighting?</h1>
  </div>
    <div class="SSL-float" align="right" style="width: 194px; margin: 0 0 15px 15px; background-color: #EEEEEE; background: url(pix/SSLbottom.gif) bottom no-repeat;">
      <p class="center"><img src="pix/SSL00000.jpg" alt="LED desk lamp with many LEDs" height="150" width="194" /></p>
      <p align="center" class="body_content_style1">Many tiny LEDs are used in<br />
        each fixture.</p>
    </div>
    <p class="body_content_style1"><br>
      LEDs are part of a family of lighting technologies called Solid-State lighting. This family also includes OLEDs (Organic Light Emitting Diodes). OLEDs (pronounced OH-leds) consist of sheets of carbon-based compounds that glow when a current is applied through transparent electrodes. While not yet market ready, OLEDs will function like a thin film on a wall or ceiling that illuminates a room. Like LEDs, OLED technology is advancing rapidly.</p>
    <p class="body_content_style1"><br>
      Solid-State lighting (SSL), most commonly seen in the form of Light Emitting Diodes (LEDs), has the potential to revolutionize the efficiency, appearance, and quality of lighting as we know it.</p>
    <p class="body_content_style1">The U.S. Department of Energy estimates that rapid adoption of LED lighting in the U.S. over the next 20 years can:</p>
    <ul>
      <li class="body_content_style1">Deliver savings of about $265 billion.</li>
      <li class="body_content_style1">Avoid 40 new power plants.</li>
      <li class="body_content_style1">Reduce lighting electricity demand by 33% in 2027.</li>
    </ul>
    
    <h1 style="padding-top:10px;">How are LED lighting products different from other lighting, like fluorescent or incandescent?</h1><br>
   
    <p class="body_content_style1">LED lighting is more efficient, durable, versatile and longer lasting than incandescent and fluorescents lighting. LEDs emit light in a specific direction, whereas an incandescent or fluorescent bulb emits light &#8212; and heat &#8212; in all directions. LED lighting uses both light and energy more efficiently.</p>
    <p class="body_content_style1">For example, an incandescent or compact fluorescent (CFL) bulb inside of a recessed can will waste about half of the light that it produces, while a recessed down light with LEDs only produces light where it&#8217;s needed &#8212; in the room below.</p><br>
    <center><div style="height: 170px; width:438px; margin-bottom: 5px; background: url(pix/IncandescentHeat-back.jpg);">
      <p align="left" class="body_content_style1" style="padding:57px 10px 0 168px; margin:0;"><strong>Incandescent bulbs</strong> create light by passing electricity through a metal filament until it becomes so hot that it glows. Incandescent bulbs release 90% of their energy as heat.</p>
    </div>
    <div style="height: 170px; width:438px; margin-bottom: 5px; background: url(pix/CFLheat-back.jpg);">
      <p align="left" class="body_content_style1" style="padding:45px 10px 0 175px; margin:0;"><strong>In a CFL</strong>, an electric current is driven through a tube containing gases. This reaction produces ultraviolet light that gets transformed into visible light by the fluorescent coating (called phosphor) on the inside of the tube. A CFL releases about 80% of its energy as heat.</p>
    </div>
    <div style="height: 180px; width:438px; margin-bottom: 5px; background: url(pix/LEDheat-back.jpg);">
      <p align="left" class="body_content_style1" style="padding:40px 10px 0 170px; margin:0;"><strong>LED lighting</strong> products use light emitting diodes to produce light very efficiently. The movement of electrons through a semiconductor material illuminates the tiny light sources we call LEDs. A small amount of heat is released backwards, into a heat sink, in a well-designed product; LEDs are basically cool to the touch.</p>
    </div></center>
    
    <h1 style="padding-top:10px;">Basic parts of LED lighting</h1><br>
    <p class="body_content_style1">LED lighting starts with a tiny chip (most commonly about one square millimeter) comprised of layers of semi-conducting material. LED packages may contain just one chip or multiple chips, mounted on heat-conducting material called a heat sink and usually enclosed in a lens. The resulting device, typically around 7 to 9 mm on a side, can be used separately or in arrays. LED devices are mounted on a circuit board, which can be programmed to include lighting controls such as dimming, light sensing and pre-set timing. The circuit board is mounted on another heat sink to manage the heat from all the LEDs in the array. The system is then encased in a lighting fixture, architectural structure, or even a &#147;light bulb&#148; package. </p>
    <p class="body_content_style1">Note: The DOE ENERGY STAR LED lighting program does not currently cover LED &#147;bulbs&#148; designed to replace regular screw-base incandescent bulbs. LED bulbs will be included as soon as these products can meet the stringent requirements of the program.</p>
    
    <h1 style="padding-top:10px; padding-bottom:5px;">Aren&#8217;t all LED lights highly efficient and long-lasting?</h1><br>
    <div class="SSL-float" style="margin-left:10px; width: 150px; background: url(pix/PurchBottom.gif) bottom no-repeat;"><img src="pix/Color_Sh.jpg" alt="LED with normal color after 100 hours" height="150" width="150" /><br />
        <img src="pix/Color_Si.jpg" alt="LED discolored after 1000 hours" height="150" width="150" style="margin-top:5px;" />
      <p  class="body_content_style1" style="font-size: 10px; padding: 5px 10px 0;">After less than a year of use, a poorly designed LED product can flicker, shift in color, look dim, offer uneven light, or continue to use power when turned off, among other problems.</p>
    </div>
    <p class="body_content_style1">Not necessarily. LEDs have been efficient and long lasting as indicator lights in electronics for years, but using LEDs to create stable white light for general lighting presents new challenges. The key to success is smart design. To qualify for ENERGY STAR, LED lighting products must pass a variety of tests to prove that the products will display the following characteristics:</p>
    <ul>
      <li class="body_content_style1"><strong>Brightness is equal to or greater than existing lighting technologies</strong> (incandescent or fluorescent) and light is well distributed over the area lighted by the fixture.</li>
      <li class="body_content_style1"><strong>Light output remains constant over time</strong>, only decreasing towards the end of the rated lifetime (at least 25,000 hours or 22 years based on use of 3 hours per day). </li>
      <li class="body_content_style1"><strong>Excellent color quality</strong>. The shade of white light appears clear and consistent over time.</li>
      <li class="body_content_style1"><strong>Efficiency is as good as or better than fluorescent lighting</strong>.</li>
      <li class="body_content_style1"><strong>Light comes on instantly</strong> when turned on.</li>
      <li class="body_content_style1"><strong>No flicker</strong> when dimmed.</li>
      <li class="body_content_style1"><strong>No off-state power draw</strong>. The fixture does not use power when it is turned off, with the exception of external controls, whose power should not exceed 0.5 watts in the off state.</li>
    </ul>
    <p class="body_content_style1"><strong>Bad design can lead to a wide range of problems, some immediately observable and some not</strong>. Poorly designed products often come with exaggerated claims while failing to deliver on the quality specifications above.</p><br>
    <p class="body_content_style1">Choose ENERGY STAR quailified LED fixtures to ensure the products you purchase perform well.</p>
