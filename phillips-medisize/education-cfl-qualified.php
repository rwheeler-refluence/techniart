<p class="body_content_style1">ENERGY STAR qualified CFLs can produce varying shades of white light.  The shade of white light is identified by the <strong>correlated color temperature</strong> or CCT, which is measured in <strong>kelvin (K)</strong>.</p>
<br>
<p class="body_content_style1">Lower kelvin numbers mean the light has a warmer color, while higher Kelvin numbers mean the light has a cooler color.</p>
<br>
<p class="body_content_style1">The majority of CFLs available in the market offer soft or warm white light (2700K&#8211;3000K), which is comparable to an incandescent bulb.</p> 
<br>
<p class="body_content_style1">This color range works well in most residential settings and enhances warmer colors (red, yellow, orange) found in your home.</p>
<br>
<p class="body_content_style1">Qualified CFLs are also available in higher kelvin color temperature CFLs (3500K, 4100K, 5000K, 6500K) and will emit more white to bluish-white light.  These products are usually identified with the terms "bright white," "natural" or "daylight."</p>
<br>
<p class="body_content_style1">These colors will enhance cooler colors (blue, green, violet) in your home.</p>


